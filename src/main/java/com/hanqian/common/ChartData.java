package com.hanqian.common;

import java.util.List;


/**
 * 图表数据
 * @author clczp
 *
 */
public class ChartData {
	private String id;				//ID
	private Number value;			//数据据
	private Comparable rowKey;		//行值
	private Comparable columnKey;	//列值
	private String mapTitle;		//热点提示信息
	private String urlTitle;		//超链接
	
		
	public ChartData() {
		super();
	}
	
	public ChartData(String id, Number value, Comparable rowKey,
			Comparable columnKey, String mapTitle, String urlTitle) {
		this.id = id;
		this.value = value;
		this.rowKey = rowKey;
		this.columnKey = columnKey;
		this.mapTitle = mapTitle;
		this.urlTitle = urlTitle;
	}

	public String getId() {
		return id;
	}
	public Number getValue() {
		return value;
	}
	public Comparable getRowKey() {
		return rowKey;
	}
	public Comparable getColumnKey() {
		return columnKey;
	}
	public String getMapTitle() {
		return mapTitle;
	}
	public String getUrlTitle() {
		return urlTitle;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setValue(Number value) {
		this.value = value;
	}
	public void setRowKey(Comparable rowKey) {
		this.rowKey = rowKey;
	}
	public void setColumnKey(Comparable columnKey) {
		this.columnKey = columnKey;
	}
	public void setMapTitle(String mapTitle) {
		this.mapTitle = mapTitle;
	}
	public void setUrlTitle(String urlTitle) {
		this.urlTitle = urlTitle;
	}

	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return "id:"+this.id+"\t rowKey:"+this.rowKey+"\t columnKey:"+this.columnKey+"\t value:"+this.value;
	}

	
	
}
