package com.hanqian.util;

public class ProcParam {
	int index; // 参数的位置序号

	String name; // 参数的名字

	Object obj; // 参数的值

	int Type; // 参数的类型 integer,string

	boolean bInputParam; // 是否是入参

	public ProcParam(int index, String name, Object obj, int type,
			boolean inputParam) {
		super();
		this.index = index;
		this.name = name;
		this.obj = obj;
		Type = type;
		bInputParam = inputParam;
	}

	/**
	 * @return the bInputParam
	 */
	public boolean isBInputParam() {
		return bInputParam;
	}

	/**
	 * @param inputParam
	 *            the bInputParam to set
	 */
	public void setBInputParam(boolean inputParam) {
		bInputParam = inputParam;
	}

	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}

	/**
	 * @param index
	 *            the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the obj
	 */
	public Object getObj() {
		return obj;
	}

	/**
	 * @param obj
	 *            the obj to set
	 */
	public void setObj(Object obj) {
		this.obj = obj;
	}

	/**
	 * @return the type
	 */
	public int getType() {
		return Type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(int type) {
		Type = type;
	}

}
