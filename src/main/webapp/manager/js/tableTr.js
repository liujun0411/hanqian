/**
* js 改变表格Tr样式文件
* @Version 1.0.0
* @author 江开军
* 
*/
$(function(){
	$('.listTable tr').mouseover(function(){
	   if($(this).attr('class')=='tr1' || $(this).attr('class')=='tr2'){
		  $(this).addClass('tr3');   
	   }
    }).mouseout(function(){
  		  $(this).removeClass('tr3');   
   });
});
